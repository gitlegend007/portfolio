let dia = parseInt(prompt("digite um numero de 1 a 7 para saber o dia da semana:"))

switch(dia){
    case 1: alert("é domingo!")
    break;
    case 2: alert("é segunda-feira!")
    break;
    case 3: alert("é terça-feira!")
    break;
    case 4: alert("é quarta-feira!")
    break;
    case 5: alert("é quinta-feira!")
    break;
    case 6: alert("é sexta-feira!")
    break;
    case 7: alert("é sabado!!")
    break;
    default: alert("numero invalido")
}
