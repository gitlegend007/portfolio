let numero = parseInt(prompt("escreva um numero positivo, negativo ou neutro:"));

if(numero < 0){
    alert("o seu numero é negativo!");
}else if(numero >=1){
    alert("o seu numero é positivo!")
}else if(numero = 0){
    alert("o seu numero é neutro!")
}else if(numero != 0){
    alert("digite apenas numero")
}